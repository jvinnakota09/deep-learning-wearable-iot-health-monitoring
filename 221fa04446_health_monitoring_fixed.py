    def run_live_monitoring(self, X_test_ecg, X_test_spo2, y_test,
                            n_samples=200, sleep_sec=0.4):
        """
        Background thread: iterates through test samples, runs real inference,
        and updates current_data so the dashboard shows live results.
        """
        print("🔗 Starting live data feed to dashboard...")
        for i in range(n_samples):
            idx        = i % len(X_test_ecg)
            ecg_win    = X_test_ecg[idx]
            spo2_win   = X_test_spo2[idx % len(X_test_spo2)]

            ecg_norm   = ecg_win.copy()
            ecg_norm   = (ecg_norm - np.mean(ecg_norm)) / (np.std(ecg_norm) + 1e-8)
            prob       = float(self.model.predict(ecg_norm.reshape(1, -1, 1), verbose=0)[0][0])
            confidence = prob if prob > 0.5 else 1.0 - prob

            self.update_data(ecg_win, spo2_win, prob, confidence)
            time.sleep(sleep_sec)
            if not self.monitoring_active:
                break
        print("✅ Live data feed complete.")

