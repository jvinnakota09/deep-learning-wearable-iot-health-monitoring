class RealTimeMonitor:
    """
    Real-time sliding-window health monitoring simulation.
    [FIX 3] IoT MQTT connection gives clear simulated feedback.
    [FIX 5] Heart rate now computed from R-peak detection, not hardcoded.
    """

    def __init__(self, model, spo2_classifier=None, buffer_size=720, fs=360):
        self.model            = model
        self.spo2_classifier  = spo2_classifier
        self.fs               = fs
        self.buffer_size      = buffer_size
        self.ecg_buffer       = deque(maxlen=buffer_size)
        self.spo2_buffer      = deque(maxlen=100)
        self.prediction_history = []
        self.alert_threshold  = 0.7
        self.mqtt_client      = None
        self._iot_simulated   = True

    # [FIX 3] — Clear IoT feedback ——————————————————————————————————————————
    def connect_iot(self, broker='localhost', port=1883):
        """
        Attempt real MQTT connection. If a broker is not available
        (expected in Colab), fall back to simulation mode with a clear
        informational message — no silent failure.
        """
        try:
            import paho.mqtt.client as mqtt
            client = mqtt.Client()
            client.connect(broker, port, keepalive=10)
            self.mqtt_client     = client
            self._iot_simulated  = False
            print(f"✅ Connected to IoT broker at {broker}:{port}")
        except Exception as e:
            self._iot_simulated = True
            self.mqtt_client    = None
            print(f"ℹ️  IoT broker not available ({e})")
            print("   → Running in SIMULATION mode: predictions logged locally.")
            print("   → In production, deploy a Mosquitto broker and re-run.")

    def add_ecg_sample(self, sample):
        self.ecg_buffer.append(sample)
        if len(self.ecg_buffer) == self.buffer_size:
            return self._process_window()
        return None

    def add_spo2_sample(self, sample):
        self.spo2_buffer.append(sample)

    def _process_window(self):
        ecg_window = np.array(self.ecg_buffer)
        ecg_norm   = (ecg_window - np.mean(ecg_window)) / (np.std(ecg_window) + 1e-8)
        ecg_input  = ecg_norm.reshape(1, self.buffer_size, 1)

        t0             = time.time()
        prob           = float(self.model.predict(ecg_input, verbose=0)[0][0])
        inference_ms   = (time.time() - t0) * 1000

        pred_label     = 1 if prob > 0.5 else 0
        confidence     = prob if pred_label == 1 else 1.0 - prob

        # [FIX 5] Real HR from R-peak detection
        hr_bpm = estimate_heart_rate(ecg_window, fs=self.fs)

        # SpO₂ classification using dedicated model if available
        spo2_status = 'N/A'
        if self.spo2_classifier and len(self.spo2_buffer) >= 100:
            spo2_win = np.array(list(self.spo2_buffer)[-100:])
            _, spo2_label, _ = self.spo2_classifier.predict_single(spo2_win)
            spo2_status = spo2_label

        result = {
            'timestamp':        datetime.now().isoformat(),
            'prediction':       pred_label,
            'probability':      prob,
            'confidence':       confidence,
            'inference_time_ms': inference_ms,
            'heart_rate_bpm':   hr_bpm,
            'spo2_status':      spo2_status,
            'status':           'ABNORMAL' if pred_label == 1 else 'NORMAL',
            'alert':            confidence > self.alert_threshold and pred_label == 1
        }
        self.prediction_history.append(result)

        if not self._iot_simulated and self.mqtt_client:
            self.mqtt_client.publish('health/monitor/ecg', json.dumps(result))

        return result

    def simulate_continuous_monitoring(self, X_test_ecg, X_test_spo2, y_test,
                                       duration_seconds=30, speedup=5):
        print(f"\n🔴 Real-Time Health Monitoring Simulation")
        print(f"   Duration: {duration_seconds}s  |  Speedup: {speedup}x  |  FS: {self.fs} Hz")
        print("=" * 65)

        start_idx = random.randint(0, len(X_test_ecg) - 1)
        processed = correct = alerts = 0

        for second in range(0, duration_seconds, speedup):
            idx         = (start_idx + second) % len(X_test_ecg)
            ecg_segment = X_test_ecg[idx].flatten()

            for sample in ecg_segment[::self.fs // 100]:
                result = self.add_ecg_sample(sample)
                if result:
                    processed += 1
                    true_label = y_test[idx]
                    if result['prediction'] == true_label:
                        correct += 1
                    if result['alert']:
                        alerts += 1
                        print(f"🚨 ALERT {result['timestamp']}: "
                              f"Abnormal | Conf: {result['confidence']:.2%} | "
                              f"HR: {result['heart_rate_bpm'] or 'N/A'} BPM")
                    elif processed % 5 == 0:
                        icon = '✅' if result['status'] == 'NORMAL' else '⚠️'
