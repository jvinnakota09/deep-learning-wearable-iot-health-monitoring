        from pyngrok import ngrok

        self.create_app()

        if use_ngrok:
            try:
                ngrok.kill()
                public_url      = ngrok.connect(self.port)
                self.ngrok_url  = str(public_url)
                print(f"🌐 Dashboard URL: {self.ngrok_url}")
            except Exception as e:
                print(f"⚠️  Ngrok: {e}  →  using local http://localhost:{self.port}")

        # [FIX 6] Wire real predictions to the dashboard
        self.monitoring_active = True
        if X_test_ecg is not None:
            live_thread = threading.Thread(
                target=self.run_live_monitoring,
                args=(X_test_ecg, X_test_spo2, y_test),
                daemon=True
            )
            live_thread.start()
        else:
            # Fallback: synthetic loop for standalone demo
            sim_thread = threading.Thread(target=self._simulation_loop, daemon=True)
            sim_thread.start()

        print(f"🚀 Starting Flask server on port {self.port}…")
        self.app.run(host='0.0.0.0', port=self.port, debug=False, use_reloader=False)

