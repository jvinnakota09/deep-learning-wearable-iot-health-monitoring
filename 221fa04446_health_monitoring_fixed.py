class HealthMonitoringModels:
    """
    Model zoo for ECG health monitoring:
      1. CNN-LSTM            — baseline
      2. Attention-BiLSTM    — proposed (best performance)
      3. Multimodal Fusion   — ECG + SpO₂ [FIX 2: proper paired data]
      4. Edge-Optimized      — lightweight for MCU deployment
    """

    @staticmethod
    def create_cnn_lstm(input_shape=(720, 1), n_classes=2, name='CNN_LSTM'):
        model = models.Sequential(name=name)
        model.add(layers.Conv1D(64, 5, activation='relu',
                                input_shape=input_shape, name='conv1'))
        model.add(layers.MaxPooling1D(2, name='pool1'))
        model.add(layers.Conv1D(128, 3, activation='relu', name='conv2'))
        model.add(layers.MaxPooling1D(2, name='pool2'))
        model.add(layers.LSTM(64, dropout=0.2, name='lstm'))
        model.add(layers.Dense(64, activation='relu', name='dense1'))
