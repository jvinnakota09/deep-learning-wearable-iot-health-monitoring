# ── 5. Background inference thread ──────────────────────────────────────────
def run_live_monitoring():
    """Cycles through test set, runs real inference, updates current_data."""
    from scipy.signal import butter, filtfilt, find_peaks
    idx = 0
    total = normal = abnormal = 0

    while True:
        try:
            ecg_win  = X_test_ecg[idx]        # (720, 1)
            spo2_win = X_test_spo2[idx]        # (100, 1)
            true_lbl = int(y_test[idx])

            # ── ECG inference ────────────────────────────────────────────────
            t0   = time.time()
            prob = float(ecg_model.predict(ecg_win[np.newaxis], verbose=0)[0][0])
            lat  = round((time.time() - t0) * 1000, 1)

            pred_ecg  = 1 if prob > 0.5 else 0
            ecg_conf  = prob if pred_ecg == 1 else 1 - prob
            ecg_label = "ABNORMAL" if pred_ecg == 1 else "NORMAL"

            # ── Heart-rate from R-peaks ───────────────────────────────────────
            sig_flat = ecg_win.flatten()
            try:
                b, a = butter(3, [5/180, 15/180], btype="band")
                filt = filtfilt(b, a, sig_flat)
                peaks, _ = find_peaks(filt,
                                      distance=int(0.3 * 360),
                                      height=0.5 * np.max(filt))
                hr = int(round(60.0 / np.mean(np.diff(peaks) / 360))) if len(peaks) >= 2 else 72
                hr = max(30, min(250, hr))
            except Exception:
                hr = 72

            # ── SpO₂ inference ───────────────────────────────────────────────
            spo2_prob = spo2_clf.model.predict(spo2_win[np.newaxis], verbose=0)[0]
            spo2_idx  = int(np.argmax(spo2_prob))
            spo2_name = ["Normal", "Warning", "Critical"][spo2_idx]
            spo2_mean = float(np.mean(spo2_win))

            # Physiological SpO₂ mean (scale model output to realistic range)
            spo2_display = {
                "Normal":   round(random.uniform(95, 99), 1),
                "Warning":  round(random.uniform(90, 94), 1),
                "Critical": round(random.uniform(82, 89), 1),
            }[spo2_name]

            # ── Update stats ─────────────────────────────────────────────────
            total    += 1
            normal   += 1 if pred_ecg == 0 else 0
            abnormal += 1 if pred_ecg == 1 else 0

            current_data.update({
                "ecg":         sig_flat.tolist(),
                "spo2":        spo2_display,
                "spo2_signal": spo2_win.flatten().tolist(),
                "heart_rate":  hr,
                "status":      ecg_label,
                "confidence":  round(ecg_conf, 4),
                "spo2_status": spo2_name,
                "model_name":  "Attention_BiLSTM",
                "latency_ms":  lat,
                "timestamp":   datetime.now().isoformat(),
                "alert":       (ecg_label == "ABNORMAL" and ecg_conf > 0.80),
                "stats": {
                    "total":    total,
                    "normal":   normal,
                    "abnormal": abnormal,
                    "accuracy": round(normal / total, 4) if total else 0,
                }
            })

            idx = (idx + 1) % len(X_test_ecg)
            time.sleep(0.5)

        except Exception as e:
            print(f"[monitor] {e}")
            time.sleep(1)

